mol.basis = '6-31+G**'
mol.build()

# ==========================================
# 2. GROUND STATE SCF
# ==========================================
print("\n--- Starting Ground State SCF ---")
mf = scf.RKS(mol)
mf.xc = "cam-b3lyp"  # range-separated hybrid functional
mf.kernel()

# ==========================================
# 3. TD-DFT (Tamm-Dancoff Approximation)
# ==========================================
print("\n--- Starting TD-DFT Calculation (10 Roots) ---")
td = tdscf.TDA(mf)
td.nroots = 10
td.kernel()

# ==========================================
# 4. TRANSITION DIPOLE EXTRACTION
# ==========================================
tdms = td.transition_dipole()

print("\n" + "="*60)
print(f"{'State':<8} {'Energy (eV)':<12} {'X':<10} {'Y':<10} {'Z':<10}")
print("-" * 60)

for i in range(td.nroots):
    tx, ty, tz = tdms[i]
    energy_ev = td.e[i] * 27.2114
    print(f"{i+1:<8} {energy_ev:<12.3f} {tx:<10.4f} {ty:<10.4f} {tz:<10.4f}")
print("="*60)